CREATE DATABASE BRASILEIRAO
USE BRASILEIRAO

CREATE TABLE TBUF
(
UF VARCHAR(2) NOT NULL,
NOMEUF VARCHAR(20),
PRIMARY KEY(UF)
)

CREATE TABLE TBEQUIPE
(
SIGLA VARCHAR(3) NOT NULL,
EQUIPE VARCHAR(25),
UF VARCHAR(2),
PRIMARY KEY(SIGLA)
)

CREATE TABLE TBRESULTADO
(
SIGLA VARCHAR(3) NOT NULL,
VITORIAS INT,
EMPATES INT,
DERROTAS INT,
PRIMARY KEY(SIGLA)
)
